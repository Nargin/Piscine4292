#include <stdlib.h>
#include <stdio.h>

int	ft_ultimate_range(int **range, int min, int max)
{
	int	i;

	if (min >= max)
	{
		*range = 0;
		return (0);
	}
	*range = (int *) malloc((max - min) * sizeof(int));
	i = 0;
	while (min < max)
		(*range)[i++] = min++;
	return (i);
}
