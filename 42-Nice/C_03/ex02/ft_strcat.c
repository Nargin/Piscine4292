char	*ft_strcat(char *dest, char *src)
{
	int	i;
	int	k;

	k = 0;
	i = 0;
	while (dest[k] != '\0')
		k++;
	while (src[i] != '\0')
	{
		dest[k + i] = src[i];
		i++;
	}
	dest[k + i] = '\0';
	return (dest);
}
